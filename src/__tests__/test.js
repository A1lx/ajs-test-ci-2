import showPlayers from '../players';

test('testing showPlayers func', () => {
  const playersList = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const resultArr = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];
  const result = showPlayers(playersList);
  expect(result).toEqual(resultArr);
});
